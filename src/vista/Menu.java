    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        
        if(txt_codigo.getText().equals("")||
           txt_descripcion.getText().equals("")||
           txt_precio.getText().equals("")||
           txt_stockMin.getText().equals("")||
           txt_stockActual.getText().equals("")||
           txt_tipoProducto.getText().equals("")){
           JOptionPane.showMessageDialog(this,"falta informacion");
           txt_descripcion.requestFocus();
            
        }
      
        else{
             try {
              String t_codigo = txt_codigo.getText();
              String descripcion = txt_descripcion.getText();
              String t_precio = txt_precio.getText();
              String t_stockMinimo = txt_stockMin.getText();
              String t_stockActual = txt_stockActual.getText();
              String tipoProducto = txt_tipoProducto.getText();

              int codigo = Integer.parseInt(t_codigo);
              int precio = Integer.parseInt(t_precio);
              int stockMinimo = Integer.parseInt(t_stockMinimo);
              int stockActual = Integer.parseInt(t_stockActual);
              ControladorProducto cp = new ControladorProducto();
              Producto producto1= cp.validar_producto(codigo);
                
                if(producto1.getStock_act()!= 0){
               
                Producto producto = new Producto(codigo,descripcion,precio,stockMinimo,stockActual,tipoProducto);
                 cp.modificarProducto(producto);
                 JOptionPane.showMessageDialog(this,"Producto codigo:"+codigo+ " modificado"); 
                }
                else{
                    JOptionPane.showMessageDialog(this,"Codigo no registrado");
                }

             
            } catch (HeadlessException | NumberFormatException e) {
                 JOptionPane.showMessageDialog(this,"en codigo precio y cantidades de stock solo puede ingresar numeros");

            }
            
        }
        
    }//GEN-LAST:event_btn_actualizarActionPerformed

    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        
        
        if(txt_codigo.getText() == null ? ("") != null : !txt_codigo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"No debe ingresar codigo para almacenar produtos");
        }
        if(txt_descripcion.getText().equals("")||
           txt_precio.getText().equals("")||
           txt_stockMin.getText().equals("")||
           txt_stockActual.getText().equals("")||
           txt_tipoProducto.getText().equals("")){
           JOptionPane.showMessageDialog(this,"falta informacion");
           txt_descripcion.requestFocus();
            
        }
        else{
          ControladorProducto cp = new ControladorProducto();

            try {
                String descripcion = txt_descripcion.getText();
                String t_precio = txt_precio.getText();
                String t_stockMinimo = txt_stockMin.getText();
                String t_stockActual = txt_stockActual.getText();
                String tipoProducto = txt_tipoProducto.getText();


                int precio = Integer.parseInt(t_precio);
                int stockMinimo = Integer.parseInt(t_stockMinimo);
                int stockActual = Integer.parseInt(t_stockActual);
       
        
            Producto producto = new Producto(1,descripcion,precio,stockMinimo,stockActual,tipoProducto);
        
            cp.agregarProducto(producto);
            JOptionPane.showMessageDialog(this,"Producto "+tipoProducto+ " registrado");
        
            } catch (HeadlessException | NumberFormatException e) {
                 JOptionPane.showMessageDialog(this,"en precio y cantidades de stock solo puede ingresar numeros");
            }
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btn_agregarActionPerformed

