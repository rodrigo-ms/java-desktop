    private void btn_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrarActionPerformed
       
         if(txt_codigo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar el codigo del producto");
            txt_codigo.requestFocus();
        
           
        }else{
              try {
                String t_codigo = txt_codigo.getText();
                int codigo = Integer.parseInt(t_codigo);
                ControladorProducto cp = new ControladorProducto();
                
                Producto producto= cp.validar_producto(codigo);
                
                if(producto.getStock_act()!= 0){
                cp.eliminar(codigo);
                JOptionPane.showMessageDialog(this,"Producto borrado");   
                }
                else{
                    JOptionPane.showMessageDialog(this,"Codigo no registrado");
                }
                
                
                
                
             
            } catch (HeadlessException | NumberFormatException e) {
                          JOptionPane.showMessageDialog(this,"Codigo no valido debe ingresar un numero");

            }
             
             
             
             
            
        }
        
        
        
    }//GEN-LAST:event_btn_borrarActionPerformed

